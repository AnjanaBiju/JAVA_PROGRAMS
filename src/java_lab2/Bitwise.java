public class Bitwise
{
    public static void main(String args[])
    {
        int x=5,y=4;
        System.out.println("x"+x);
        System.out.println("negation of x is"+~x);
        System.out.println("y"+y);
        System.out.println("negation of y is"+~y);
        System.out.println("x & y ="+(x&y));
        System.out.println("x|y = "+(x|y));
        System.out.println("x ^ y ="+ (x^y));
        
    }
}
