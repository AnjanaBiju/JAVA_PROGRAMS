public class Unary 
{
    public static void main(String args[])
    {
       
     int x=4,y=7;
        System.out.println("x=" +x);
        System.out.println("y=" + y);
        System.out.println("x=" + ++x);
        System.out.println("y=" + y++);
        System.out.println("x=" +x);
        System.out.println("y=" + y);
    
    }
}
