public class Relational 
{
    public static void main( String args[])
    {
        int a=5,b=6;
        System.out.println("A="+a);
        System.out.println("B="+b);
        System.out.println("A>B is " +(a>b));
        System.out.println("A<B is " +(a<b));
        System.out.println("A>=B is " +(a>=b));
        System.out.println("A<=B is " +(a<=b));
        System.out.println("A==B is " +(a==b));
        System.out.println("A!=B is " +(a!=b));
    }
}
